import React from 'react';

import '../css/Tile.css'; 

class Tile extends React.Component {

  render() {
    return <div 
      className="tile" 
      style={this.props.style}
      draggable="true"
      onTouchStart={this.props.startDrag}
      onTouchMove={this.props.moveDrag}
      onTouchEnd={this.props.endDrag}
    />
  }
}

export default Tile;

